int		is_solved(char **arr)
{
	int	i;
	int	j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (arr[i][j] == '0')
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}